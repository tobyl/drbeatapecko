			<div class="panel-body">
				<p>Sugar is the main cause of dental decay when there is bacteria present. More important than the amount of sugar you take is the frequency of it.</p>
				<p>The best way to prevent cavities is to prevent the sugar from staying next to your teeth. Brushing after eating sugar, rinsing your mouth with Fluoride mouth wash or chewing sugarless gum can help. But nothing has the effect of avoiding sugar!</p>
				<p>Another group of food that causes significant damage to teeth structure is acidic foods. Things like lemon, lime and grapefruit, if in frequent contact with teeth, can cause serious irreversible damage (erosion) to your teeth.</p>

