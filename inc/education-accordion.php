			<div class="panel-body">
				<p>There are a few different causes for bad breath.  It can range from stomach problems to diets and teeth problems. Most of the causes can be found in the mouth, they are:</p>
				<ol>
					<li>Tongue</li>
					<li>Teeth cavities</li>
					<li>Gum disease</li>
					<li>Extraction sites while healing</li>
					<li>Dentures when not cleaned properly and regularly</li>
					<li>Alcohol and tobacco</li>
				</ol>
				<p>Non dental reasons that cause bad breath:</p>
				<ol>
					<li>Sore throat</li>
					<li>Tonsillitis</li>
					<li>Some foods</li>
					<li>Infection of air passages</li>
